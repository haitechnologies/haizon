<h2>1. Our Security Commitment</h2>
<p>Security is a top priority at HAIPULSE. We implement comprehensive security measures to protect your personal information, account data, and transactions from unauthorized access, disclosure, or misuse.</p>

<h2>2. Technical Security Measures</h2>

<h3>2.1 Encryption</h3>
<ul>
<li><strong>SSL/TLS Encryption:</strong> All data transmitted between your browser and our servers is encrypted using industry-standard SSL/TLS protocols</li>
<li><strong>HTTPS:</strong> Our website uses HTTPS to ensure secure connections</li>
<li><strong>Password Encryption:</strong> Passwords are encrypted using bcrypt hashing with salt</li>
<li><strong>Data at Rest:</strong> Sensitive data is encrypted when stored in our database</li>
</ul>

<h3>2.2 Firewalls and Intrusion Detection</h3>
<ul>
<li>Web Application Firewalls (WAF) protect against common attacks</li>
<li>Intrusion Detection Systems monitor for unauthorized access attempts</li>
<li>DDoS protection safeguards against denial-of-service attacks</li>
</ul>

<h3>2.3 Server Security</h3>
<ul>
<li>Regular security patching and updates</li>
<li>Principle of least privilege for user access</li>
<li>Secure server configuration and hardening</li>
<li>Regular vulnerability scanning and penetration testing</li>
</ul>

<h2>3. Account Security</h2>

<h3>3.1 Password Security</h3>
<ul>
<li>Strong password requirements (minimum complexity)</li>
<li>Password reset functionality with verification</li>
<li>Two-factor authentication (2FA) available</li>
<li>Session timeouts for added security</li>
</ul>

<h3>3.2 Access Control</h3>
<ul>
<li>Role-based access control (RBAC)</li>
<li>Failed login attempt monitoring and lockout</li>
<li>Session management and token validation</li>
<li>CSRF (Cross-Site Request Forgery) token protection</li>
</ul>

<h2>4. Data Protection Practices</h2>
<ul>
<li>Regular backups of all critical data</li>
<li>Secure data disposal procedures</li>
<li>Access logs and audit trails maintained</li>
<li>Data minimization - we collect only necessary information</li>
<li>Third-party vendor security vetting</li>
</ul>

<h2>5. Employee Security</h2>
<ul>
<li>Mandatory security training for all staff</li>
<li>Background checks for employees with data access</li>
<li>Non-disclosure agreements (NDAs) required</li>
<li>Limited access to sensitive data on need-to-know basis</li>
</ul>

<h2>6. Payment Security</h2>
<ul>
<li>PCI DSS compliance for payment processing</li>
<li>Tokenization of payment information</li>
<li>Third-party payment processors certified for security</li>
<li>No storage of full credit card numbers</li>
</ul>

<h2>7. Incident Response</h2>
<p>In case of a security breach, we will:</p>
<ul>
<li>Investigate the incident immediately</li>
<li>Notify affected users as required by law</li>
<li>Take corrective measures</li>
<li>Cooperate with law enforcement as needed</li>
<li>Document and learn from incidents</li>
</ul>

<h2>8. Your Security Responsibilities</h2>
<ul>
<li>Choose a strong, unique password</li>
<li>Never share your login credentials</li>
<li>Log out when finished, especially on shared devices</li>
<li>Keep your browser updated</li>
<li>Report suspicious activity immediately</li>
</ul>

