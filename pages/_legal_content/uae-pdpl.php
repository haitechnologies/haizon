<h2>1. UAE PDPL Overview</h2>
<p>The UAE Personal Data Protection Law (PDPL) is the United Arab Emirates' comprehensive personal data protection regulation. It applies to organizations processing personal data within the UAE. HAIPULSE fully complies with PDPL requirements.</p>

<h2>2. Our PDPL Commitment</h2>
<p>HAIPULSE is committed to:</p>
<ul>
<li>Processing personal data lawfully and fairly</li>
<li>Collecting data only for clear and specified purposes</li>
<li>Ensuring data accuracy and completeness</li>
<li>Maintaining confidentiality and security</li>
<li>Respecting individual privacy rights</li>
<li>Operating transparently in data handling</li>
</ul>

<h2>3. Data Subject Rights</h2>

<h3>3.1 Right to Know</h3>
<p>You have the right to:</p>
<ul>
<li>Know what personal data we have about you</li>
<li>Understand purposes of data collection and processing</li>
<li>Know who has access to your data</li>
<li>Request this information in writing</li>
<li>Receive response within 30 days</li>
</ul>

<h3>3.2 Right of Access</h3>
<p>You can request:</p>
<ul>
<li>A copy of your personal data</li>
<li>Information in clear, understandable language</li>
<li>Access at no cost (usually)</li>
<li>Access in reasonable frequency</li>
</ul>

<h3>3.3 Right to Correction</h3>
<p>You may request:</p>
<ul>
<li>Correction of inaccurate data</li>
<li>Completion of incomplete data</li>
<li>Updates to outdated information</li>
<li>Verification of accuracy</li>
</ul>

<h3>3.4 Right to Erasure</h3>
<p>You can request deletion of personal data if:</p>
<ul>
<li>No longer necessary for original purposes</li>
<li>Unlawfully processed</li>
<li>No legal basis for retention</li>
<li>Exception: Legal obligations requiring retention</li>
</ul>

<h3>3.5 Right to Object</h3>
<p>You may object to processing when:</p>
<ul>
<li>Processing violates your rights</li>
<li>Causing you harm or damage</li>
<li>Infringing upon privacy</li>
</ul>

<h3>3.6 Right to Restrict Processing</h3>
<p>You can request limitation of data processing:</p>
<ul>
<li>During disputes about accuracy</li>
<li>When processing is unlawful</li>
<li>When data is no longer needed for original purpose</li>
</ul>

<h2>4. Personal Data Categories</h2>
<p>The PDPL defines personal data as any information relating to an identified or identifiable natural person:</p>
<ul>
<li>Name and contact information (email, phone, address)</li>
<li>Identity verification data (ID number, passport)</li>
<li>Financial information (bank accounts, payment methods)</li>
<li>Employment data (position, organization)</li>
<li>Biometric data (fingerprints, facial recognition)</li>
<li>Health and medical information</li>
<li>Online identifiers (IP address, cookies)</li>
<li>Location data</li>
<li>Sensitive personal data (race, religion, political affiliation)</li>
</ul>

<h2>5. Sensitive Personal Data</h2>
<p>We handle sensitive data with heightened protection:</p>
<ul>
<li>Biometric data: Extra security measures</li>
<li>Health data: Restricted access and processing</li>
<li>Financial data: Encryption and secure storage</li>
<li>Processing only with explicit consent (where required)</li>
<li>Minimal retention periods</li>
</ul>

<h2>6. Legal Basis for Processing</h2>
<p>HAIPULSE processes personal data only based on:</p>
<ul>
<li><strong>Explicit Consent:</strong> You've given clear, informed permission</li>
<li><strong>Contract:</strong> Processing necessary to fulfill our service agreement</li>
<li><strong>Legal Obligation:</strong> Required by UAE law or regulation</li>
<li><strong>Vital Interest:</strong> Protection of health and life</li>
<li><strong>Public Task:</strong> Official authority of a federal agency</li>
<li><strong>Legitimate Interest:</strong> Our business operations (when outweighing individual rights)</li>
</ul>

<h2>7. Data Collection Standards</h2>
<p>We collect personal data only when:</p>
<ul>
<li>Necessary for stated purposes</li>
<li>Collected fairly and transparently</li>
<li>Limited to what's essential</li>
<li>Relevant and not excessive</li>
<li>Kept accurate and up-to-date</li>
</ul>

<h2>8. Data Retention</h2>
<p>We retain personal data only as long as necessary:</p>
<ul>
<li>Account information: Duration of account + 2 years</li>
<li>Transaction records: 7 years (legal requirement)</li>
<li>Communication data: 3 years after last contact</li>
<li>Marketing data: Until withdrawal of consent</li>
<li>Support records: 3 years after resolution</li>
</ul>

<h2>9. Data Security</h2>
<p>HAIPULSE implements security measures to protect personal data:</p>
<ul>
<li><strong>Encryption:</strong> Data encrypted in transit (TLS/SSL) and at rest</li>
<li><strong>Access Control:</strong> Limited access to authorized personnel only</li>
<li><strong>Authentication:</strong> Strong password policies and 2FA</li>
<li><strong>Infrastructure:</strong> Secure servers and firewalls</li>
<li><strong>Monitoring:</strong> Continuous security monitoring and audits</li>
<li><strong>Training:</strong> Employee data protection training</li>
</ul>

<h2>10. Sub-processors and Third Parties</h2>
<p>When processing with third parties:</p>
<ul>
<li>Contracts in place protecting your data</li>
<li>Third parties bound by same PDPL obligations</li>
<li>Sub-processor list available upon request</li>
<li>You can request information about third-party processors</li>
</ul>

<h2>11. Data Breach Notification</h2>
<p>In case of unauthorized data access:</p>
<ul>
<li>We notify affected individuals immediately</li>
<li>We notify PDPL Authority within required timeframe</li>
<li>We explain nature, scope, and consequences of breach</li>
<li>We provide steps to mitigate effects</li>
<li>We document all breach information</li>
</ul>

