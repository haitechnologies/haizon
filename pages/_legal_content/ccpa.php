<h2>1. CCPA and CPRA Overview</h2>
<p>The California Consumer Privacy Act (CCPA) and California Privacy Rights Act (CPRA) are California state privacy laws that grant California residents specific rights regarding their personal information. HAIPULSE fully complies with these laws.</p>

<h2>2. Covered Information</h2>
<p>CCPA/CPRA covers "personal information" which includes information that identifies, relates to, or could be reasonably linked with a particular consumer:</p>
<ul>
<li>Name, address, email, phone number</li>
<li>Account credentials and authentication data</li>
<li>Payment and financial information</li>
<li>Device identifiers and IP addresses</li>
<li>Commercial information (purchase history, transactions)</li>
<li>Biometric information</li>
<li>Health and fitness data</li>
<li>Geolocation data</li>
<li>Inferences drawn from personal information</li>
</ul>

<h2>3. Your CCPA/CPRA Rights</h2>

<h3>3.1 Right to Know (Access)</h3>
<p>You have the right to request:</p>
<ul>
<li>What personal information we collect about you</li>
<li>The sources of that information</li>
<li>Our purposes for collection</li>
<li>Categories of third parties with access</li>
<li>A copy of the specific data we hold</li>
</ul>

<h3>3.2 Right to Delete</h3>
<p>You may request deletion of personal information we've collected from you, with exceptions including:</p>
<ul>
<li>Information needed to complete transactions</li>
<li>Information required by law</li>
<li>Information for security and fraud prevention</li>
<li>Business necessity information</li>
</ul>

<h3>3.3 Right to Correct (CPRA)</h3>
<p>You can request correction of inaccurate personal information we hold about you.</p>

<h3>3.4 Right to Opt-Out of Sales/Sharing</h3>
<p>You have the right to:</p>
<ul>
<li>Opt-out of the sale of personal information</li>
<li>Opt-out of sharing for cross-context behavioral advertising</li>
<li>Opt-out of profiling in furtherance of decisions that produce legal/similarly significant effects</li>
</ul>

<h3>3.5 Right to Limit Use and Disclosure</h3>
<p>You can request we limit use of sensitive personal information to:</p>
<ul>
<li>Performing services reasonably expected</li>
<li>Preventing security incidents</li>
<li>Compliance with law</li>
</ul>

<h3>3.6 Right to Non-Discrimination (CCPA/CPRA)</h3>
<p>We will not discriminate against you for exercising your rights:</p>
<ul>
<li>No denial of goods or services</li>
<li>No different pricing or rates</li>
<li>No different quality of service</li>
<li>We will not penalize you for privacy choices</li>
</ul>

<h3>3.7 Right to Authorize an Agent</h3>
<p>You may designate an authorized agent to submit requests on your behalf. The agent needs:</p>
<ul>
<li>Written authorization from you</li>
<li>Or proof of power of attorney</li>
<li>Or notarized verification</li>
</ul>

