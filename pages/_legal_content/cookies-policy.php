<h2>1. What Are Cookies?</h2>
<p>Cookies are small text files stored on your device when you visit our website. They contain information that helps us improve your browsing experience, remember preferences, and understand how you use HAIPULSE.</p>

<h2>2. Types of Cookies We Use</h2>

<h3>2.1 Essential Cookies</h3>
<p>These cookies are necessary for the website to function properly:</p>
<ul>
<li>Session identification and authentication</li>
<li>Security and fraud prevention</li>
<li>Login credentials management</li>
<li>CSRF token validation</li>
</ul>

<h3>2.2 Performance Cookies</h3>
<p>These cookies help us understand how you interact with our site:</p>
<ul>
<li>Page view tracking</li>
<li>Performance metrics collection</li>
<li>Error detection and reporting</li>
<li>Analytics and usage patterns</li>
</ul>

<h3>2.3 Functional Cookies</h3>
<p>These enhance your user experience:</p>
<ul>
<li>Remember user preferences</li>
<li>Store language and region settings</li>
<li>Maintain shopping cart contents</li>
<li>Save form information</li>
</ul>

<h3>2.4 Marketing Cookies</h3>
<p>These help us deliver relevant content and advertisements:</p>
<ul>
<li>Track your interests and behavior</li>
<li>Deliver personalized advertisements</li>
<li>Create audience segments for retargeting</li>
<li>Measure campaign effectiveness</li>
</ul>

<h2>3. Third-Party Cookies</h2>
<p>We work with third-party service providers (Google Analytics, payment processors, etc.) that may set their own cookies. These are governed by their respective privacy policies.</p>

<h2>4. Cookie Duration</h2>
<ul>
<li><strong>Session Cookies:</strong> Deleted when you close your browser</li>
<li><strong>Persistent Cookies:</strong> Remain on your device for a specified period (up to 2 years)</li>
</ul>

<h2>5. Managing Your Cookies</h2>
<p>You have control over cookies through your browser settings:</p>
<ul>
<li>Accept or reject cookies</li>
<li>Delete existing cookies</li>
<li>Block cookies from specific sites</li>
<li>Clear cookies automatically on browser exit</li>
</ul>

<h2>6. Do Not Track Signals</h2>
<p>If your browser sends a "Do Not Track" (DNT) signal, we will respect your preference and limit tracking. However, some services may not function optimally without cookies.</p>

<h2>7. Cookie-Free Experience</h2>
<p>You can use HAIPULSE without cookies, but some features may be limited:</p>
<ul>
<li>You may need to login repeatedly</li>
<li>Personalized recommendations will be unavailable</li>
<li>Some interactive features may not work</li>
</ul>

<h2>8. Changes to This Policy</h2>
<p>We may update this Cookie Policy as our use of cookies evolves. We will notify you of significant changes.</p>

