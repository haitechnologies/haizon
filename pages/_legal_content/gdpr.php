<h2>1. GDPR Overview</h2>
<p>The General Data Protection Regulation (GDPR) is the European Union's comprehensive privacy law that applies to organizations processing personal data of EU/EEA residents. HAIPULSE complies fully with GDPR requirements.</p>

<h2>2. Our GDPR Commitment</h2>
<p>HAIPULSE commits to:</p>
<ul>
<li>Processing personal data lawfully, fairly, and transparently</li>
<li>Collecting data only for specified, explicit, and legitimate purposes</li>
<li>Maintaining data accuracy and keeping personal data up-to-date</li>
<li>Retaining data only as long as necessary</li>
<li>Ensuring integrity and confidentiality of personal data</li>
</ul>

<h2>3. Legal Basis for Data Processing</h2>
<p>We process your personal data based on:</p>
<ul>
<li><strong>Consent:</strong> You've explicitly agreed to data processing</li>
<li><strong>Contract:</strong> Processing necessary to fulfill our service agreement</li>
<li><strong>Legal Obligation:</strong> Required by law or regulation</li>
<li><strong>Legitimate Interest:</strong> Processing necessary for our business operations</li>
<li><strong>Vital Interest:</strong> Protection of life and health</li>
</ul>

<h2>4. Your GDPR Rights</h2>

<h3>4.1 Right to Access (Article 15)</h3>
<p>You have the right to:</p>
<ul>
<li>Request a copy of all personal data HAIPULSE holds about you</li>
<li>Receive information about our data processing practices</li>
<li>Know the purposes and legal basis for processing</li>
<li>Request this at any time free of charge</li>
</ul>

<h3>4.2 Right to Rectification (Article 16)</h3>
<p>You can:</p>
<ul>
<li>Request correction of inaccurate personal data</li>
<li>Ask us to complete incomplete information</li>
<li>Update your profile information anytime</li>
</ul>

<h3>4.3 Right to Erasure (Article 17)</h3>
<p>You have the "right to be forgotten":</p>
<ul>
<li>Request deletion of your personal data</li>
<li>Applicable when data is no longer necessary</li>
<li>Exceptions: Legal obligations, legitimate interests</li>
<li>Processing typically completed within 30 days</li>
</ul>

<h3>4.4 Right to Restrict Processing (Article 18)</h3>
<p>You can request we limit how we use your data:</p>
<ul>
<li>While disputing accuracy</li>
<li>When processing is unlawful but you prefer restriction</li>
<li>When the data is no longer needed but you require it for legal claims</li>
</ul>

<h3>4.5 Right to Data Portability (Article 20)</h3>
<p>You have the right to:</p>
<ul>
<li>Receive a copy of your data in machine-readable format</li>
<li>Transmit that data to another service provider</li>
<li>Applicable when processing is based on consent or contract</li>
</ul>

<h3>4.6 Right to Object (Article 21)</h3>
<p>You can object to:</p>
<ul>
<li>Processing based on legitimate interests</li>
<li>Direct marketing and profiling</li>
<li>Automated decision-making</li>
</ul>

<h3>4.7 Rights Regarding Automated Decision-Making (Article 22)</h3>
<p>You have the right to:</p>
<ul>
<li>Not be subject to decisions based solely on automated processing</li>
<li>Request human review of automated decisions</li>
<li>Express your views and contest automated decisions</li>
</ul>

